    private void addSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addSubmitMouseClicked
        final String fristName = fNameTexted.getText();
        final String lastName = lNameTexted.getText();
        final String email = emailTexted.getText();
        final String country = countryTexted.getText();
                
        UserDAO userDao;
        UserModel newUser = new UserModel(0, fristName, lastName, email, country);
        try{
            
            if(fristName.isEmpty()){
            JOptionPane.showMessageDialog(this, "first name is required", "error", JOptionPane.ERROR_MESSAGE);
            } else if(lastName.isEmpty()){
                JOptionPane.showMessageDialog(this, "last name is required", "error", JOptionPane.ERROR_MESSAGE);
            } else if(email.isEmpty()){
                JOptionPane.showMessageDialog(this, "email is required", "error", JOptionPane.ERROR_MESSAGE);
            }else if(country.isEmpty()){
                JOptionPane.showMessageDialog(this, "country is required", "error", JOptionPane.ERROR_MESSAGE);
            }else{
                userDao = new UserDAO();
                int result = userDao.insertUser(newUser);
                    this.clearForm();
                if(result == 1) {
                    JOptionPane.showMessageDialog(this, "new user data create successfully", "success", JOptionPane.INFORMATION_MESSAGE);
                    loadUsers();
                }else {
                    JOptionPane.showMessageDialog(this, "Error occured while inserting data", "error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, "Error occured while inserting data " +e.getMessage(), "error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addSubmitMouseClicked

    private void usersTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usersTableMouseClicked
        int selectedRow = usersTable.getSelectedRow();
        if (selectedRow != -1) {
            selectedUserId = Integer.parseInt(usersTable.getValueAt(selectedRow, 0).toString());
            fNameTexted.setText(usersTable.getValueAt(selectedRow, 1).toString());
            lNameTexted.setText(usersTable.getValueAt(selectedRow, 2).toString());
            emailTexted.setText(usersTable.getValueAt(selectedRow, 3).toString());
            countryTexted.setText(usersTable.getValueAt(selectedRow, 4).toString());
        }
    }//GEN-LAST:event_usersTableMouseClicked

    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        String firstName = fNameTexted.getText();
        String lastName = lNameTexted.getText();
        String email = emailTexted.getText();
        String country = countryTexted.getText();

    if (selectedUserId == -1) {
        JOptionPane.showMessageDialog(this, "No user selected!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }else if(firstName.isEmpty()){
            JOptionPane.showMessageDialog(this, "first name is required", "error", JOptionPane.ERROR_MESSAGE);
    } else if(lastName.isEmpty()){
        JOptionPane.showMessageDialog(this, "last name is required", "error", JOptionPane.ERROR_MESSAGE);
    } else if(email.isEmpty()){
        JOptionPane.showMessageDialog(this, "email is required", "error", JOptionPane.ERROR_MESSAGE);
    }else if(country.isEmpty()){
        JOptionPane.showMessageDialog(this, "country is required", "error", JOptionPane.ERROR_MESSAGE);
    }else {
        UserModel updatedUser = new UserModel(selectedUserId, firstName, lastName, email, country);
        try {
            UserDAO userDao = new UserDAO();
            boolean success = userDao.updateUser(updatedUser);
            if (success) {
                JOptionPane.showMessageDialog(this, "User updated successfully.");
                loadUsers();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update user.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_updateMouseClicked

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
       if(selectedUserId != -1) {
            int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this","Dialog", JOptionPane.YES_NO_OPTION);
            if(confirmation == JOptionPane.YES_OPTION){
                UserDAO userDAO;
                try{
                    userDAO = new UserDAO();
                    userDAO.deleteUser(selectedUserId);
                    JOptionPane.showMessageDialog(this,"Delete user data successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.clearForm();
                    loadUsers();
                    selectedUserId = -1;
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(this,"Error while Deleted user data " + e, "Eror", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else{
            JOptionPane.showMessageDialog(this,"No data selected for delete user data", "Eror", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteMouseClicked

