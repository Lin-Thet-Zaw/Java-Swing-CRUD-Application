    public final void loadUsers()throws SQLException {
        UserDAO userDAO = new UserDAO();
        List<UserModel> users = userDAO.getUsers();
        String[][] data = new String[users.size()][5];
        
        for(int i=0; i<users.size(); i++) {
            data[i][0] = String.valueOf(users.get(i).getId());
            data[i][1] = users.get(i).getFristName();
            data[i][2] = users.get(i).getLastName();
            data[i][3] = users.get(i).getEmail();
            data[i][4] = users.get(i).getCountry();
        }
        String[] columName = {"ID", "Frist Name", "Last Name", "Email", "Country"};
        usersTable.setModel(new javax.swing.table.DefaultTableModel(data, columName));
    }
    
